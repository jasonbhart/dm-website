def build_horizontal_svg(icon_color, domain_color, methods_color, tagline_color, bg_transparent=True):
    """
    Build horizontal logo SVG.

    Layout (viewBox 3400 520):
    - Icon on the left, scaled to ~350 tall
    - "DOMAIN METHODS" to the right, vertically centered with icon
    - "DATA AND ANALYTICS EXCELLENCE" below, left-aligned with DOMAIN

    The icon is in a 1000x1000 coordinate space.
    The text paths are in a 4000x4000 coordinate space.
    We use SVG transform groups to position everything in a 3400x520 viewBox.
    """
    # Icon: scale 0.52 to make it ~350 tall (673*0.52=350)
    # Translate to vertically center in 520 viewBox: center at y=260
    # Icon center in its own space: (164+837)/2 = 500.5
    # After scale: 500.5*0.52 = 260.3, so translate y=0 centers it perfectly
    # Horizontal: translate x=20 for left padding
    icon_scale = 0.52
    icon_tx = 20
    icon_ty = 0

    # DOMAIN text: translate from 4000-space to viewBox
    # Original DOMAIN starts at x≈884, y≈2151
    # Want left edge at x=500, top at y≈105
    # Use scale 1.0 (keep original proportions from 4000 space)
    # Scale factor to make text proportional to icon:
    # DOMAIN METHODS height is 161 in 4000-space
    # Icon is 350 tall in viewBox. We want DOMAIN METHODS about 160 tall.
    # At text_scale=1.0, height=161. Good.
    text_scale = 1.0
    text_left = 580

    # DOMAIN METHODS: original y range 2151 to 2312 (height 161)
    # Tagline: original y range 2389 to 2462 (height 73)
    # Gap between them: 2389-2312 = 77
    # Total text block height: 2462-2151 = 311
    # Center of text block at y=260: top at 260-155.5=104.5
    text_top = 105

    # Transform for DOMAIN + METHODS text
    dm_tx = text_left - 884 * text_scale  # 500 - 884 = -384
    dm_ty = text_top - 2151 * text_scale  # 105 - 2151 = -2046

    # Transform for tagline (same scale, left-aligned with DOMAIN)
    # Tagline original x starts at 1010
    tl_tx = text_left - 1010 * text_scale  # 500 - 1010 = -510
    tl_ty = dm_ty  # Same y transform to preserve relative positioning

    bg = "" if bg_transparent else '<rect width="3400" height="520" fill="white"/>'

    domain_paths_str = "\n    ".join(f'<path d="{p}"/>' for p in DOMAIN_PATHS)
    methods_paths_str = "\n    ".join(f'<path d="{p}"/>' for p in METHODS_PATHS)
    tagline_paths_str = "\n    ".join(f'<path d="{p}"/>' for p in TAGLINE_PATHS)

    svg = f"""<?xml version="1.0" encoding="utf-8"?>
<svg viewBox="0 0 3480 520" xmlns="http://www.w3.org/2000/svg">
  {bg}
  <!-- Icon -->
  <g transform="translate({icon_tx}, {icon_ty}) scale({icon_scale})" fill="{icon_color}">
    <path d="{ICON_PATH}"/>
  </g>

  <!-- DOMAIN text -->
  <g transform="translate({dm_tx}, {dm_ty}) scale({text_scale})" fill="{domain_color}">
    {domain_paths_str}
  </g>

  <!-- METHODS text -->
  <g transform="translate({dm_tx}, {dm_ty}) scale({text_scale})" fill="{methods_color}">
    {methods_paths_str}
  </g>

  <!-- DATA AND ANALYTICS EXCELLENCE tagline -->
  <g transform="translate({tl_tx}, {tl_ty}) scale({text_scale})" fill="{tagline_color}">
    {tagline_paths_str}
  </g>
</svg>"""
    return svg


def main():
    output_dir = os.path.join(PROJECT_ROOT, "assets", "images")

    # Light mode: blue icon, blue DOMAIN, pink METHODS, blue tagline
    light_svg = build_horizontal_svg(
        icon_color=BLUE,
        domain_color=BLUE,
        methods_color=PINK,
        tagline_color=BLUE,
    )

    # Dark mode: white icon, white DOMAIN, pink METHODS, white tagline
    dark_svg = build_horizontal_svg(
        icon_color=WHITE,
        domain_color=WHITE,
        methods_color=PINK,
        tagline_color=WHITE,
    )

    # Save SVGs for reference
    svg_dir = os.path.join(PROJECT_ROOT, "assets", "logo")
    light_svg_path = os.path.join(svg_dir, "logo-horizontal.svg")
    dark_svg_path = os.path.join(svg_dir, "logo-horizontal-darkmode.svg")

    with open(light_svg_path, "w") as f:
        f.write(light_svg)
    print(f"Saved: {light_svg_path}")

    with open(dark_svg_path, "w") as f:
        f.write(dark_svg)
    print(f"Saved: {dark_svg_path}")

    # Convert to PNG at 2x resolution for crisp display
    # Output at 2000px wide (will display at ~300px in navbar)
    png_width = 2000
    png_height = int(2000 * 520 / 3480)  # Maintain aspect ratio

    light_png_path = os.path.join(output_dir, "logo.png")
    dark_png_path = os.path.join(output_dir, "logo-darkmode.png")

    cairosvg.svg2png(
        bytestring=light_svg.encode("utf-8"),
        write_to=light_png_path,
        output_width=png_width,
        output_height=png_height,
    )
    print(f"Saved: {light_png_path} ({png_width}x{png_height})")

    cairosvg.svg2png(
        bytestring=dark_svg.encode("utf-8"),
        write_to=dark_png_path,
        output_width=png_width,
        output_height=png_height,
    )
    print(f"Saved: {dark_png_path} ({png_width}x{png_height})")


if __name__ == "__main__":
    main()
